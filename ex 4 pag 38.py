'''4. Elabore um programa que receba quatro notas e calcule a média aritmética entre
elas.'''

# Recebendo as notas
nota_1 = float(input("Dígite uma nota"))
nota_2 = float(input("Dígite uma nota"))
nota_3 = float(input("Dígite uma nota"))
nota_4 = float(input("Dígite uma nota"))

# Calcula a média
media = (nota_1 + nota_2 + nota_3 + nota_4) / 4

# Imprime a média
print("A média entre as notas é:", media)